<script>
    const stars = document.querySelectorAll('.star');
    const rateInput = document.getElementById('rate');

    stars.forEach(star => {
        star.addEventListener('click', () => {
            const rating = star.getAttribute('data-rating');
            rateInput.value = rating;
            highlightStars(rating);
        });
    });

    function highlightStars(rating) {
        stars.forEach(star => {
            if (star.getAttribute('data-rating') <= rating) {
                star.style.color = 'gold';
            } else {
                star.style.color = 'black';
            }
        });
    }
</script>
<script>
    const feedbackTextarea = document.getElementById('feedback_msg');
    const charCountSpan = document.getElementById('charCount');
    const maxLength = parseInt(feedbackTextarea.getAttribute('maxlength'));

    feedbackTextarea.addEventListener('input', function () {
        const remainingChars = maxLength - feedbackTextarea.value.length;
        charCountSpan.textContent = remainingChars + '/200';

        if (remainingChars < 0) {
            charCountSpan.style.color = 'red';
        } else {
            charCountSpan.style.color = 'gray';
        }
    });
</script>

